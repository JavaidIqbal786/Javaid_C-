#include <iostream>
using namespace std;

int main(){
	// Data Types
	int a = 13;
	short b = 5;
	float c = 20.56;
	double d = 1.584683;
	long double e = 5.55546;
	int const f = 7;
	cout<<"a= "<<a<<endl;
	cout<<"b= "<<b<<endl;
	cout<<"c= "<<c<<endl;
	cout<<"d= "<<d<<endl;
	cout<<"e= "<<e<<endl;
	cout<<"f= "<<f<<endl;
	// Operators
	cout<<"a + b = "<<a + b<<endl;
	cout<<"a - b = "<<a - b<<endl;
	cout<<"a * b = "<<a * b<<endl;
	cout<<"a / b = "<<a / b<<endl;
	cout<<"a % b = "<<a % b<<endl;
	
	int x, y;
	cout<<"Enter the value of x "; // Taking input from the user
	cin>>x;
	cout<<"Enter the value of y ";
	cin>>y;
	return 0;
}
